//Your First C++ Program

#include <iostream>
#include <cmath>

using namespace std;

int main() {
	
	cout << pow(3, 3)    << endl; // 27
	cout << sqrt(64)     << endl; // 8
	cout << round(4.5)   << endl; // 5
	cout << round(4.3)   << endl; // 4
	cout << ceil(4.1)    << endl; // 5
	cout << floor(4.9)   << endl; // 4
	cout << fmax(3, 10)  << endl; // 10
	cout << fmin(3, 10)  << endl; // 3
	
	return 0;
}